#!/usr/bin/env bash
# 02_download_datasets.sh -- download all datasets used in the paper.
#
# Note: some datasets are large and gated; users may need to register or
# accept terms before downloading. We provide canonical URLs and SHA256
# checksums where available.
#
# Disk requirements:
#   YFCC10M  ~12 GB (vectors + labels + GT)
#   SIFT1M   ~0.5 GB
#   SIFT100M ~12 GB
#   SIFT1B   ~120 GB (raw) + ~250 GB (decompressed)
#   GIST1M   ~4 GB
#   DEEP10M  ~4 GB
#   LAION1M  ~2 GB (vectors only; tags scraped separately)
#
set -euo pipefail
ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
DATA="${BOOLEANN_DATA_DIR:-$ROOT/data/raw}"
mkdir -p "$DATA"
cd "$DATA"

dl() {
  local url="$1" out="$2"
  if [ -f "$out" ]; then echo "[skip] $out exists"; return; fi
  echo "[get] $out from $url"
  curl -L --fail --retry 3 -o "$out" "$url" || {
    echo "[warn] curl failed for $out; please download manually from $url"
  }
}

############################################################
# 1. YFCC10M (BigANN Filter Track, 10M-vector subset of YFCC100M)
############################################################
mkdir -p yfcc100m && cd yfcc100m
# Big-ANN-benchmarks YFCC filter track artifacts
dl "https://comp21storage.z5.web.core.windows.net/comp23/filter/base.10M.u8bin"               "base.10M.u8bin"
dl "https://comp21storage.z5.web.core.windows.net/comp23/filter/query.public.100K.u8bin"      "query.public.100K.u8bin"
dl "https://comp21storage.z5.web.core.windows.net/comp23/filter/base.metadata.10M.spmat"      "base.metadata.10M.spmat"
dl "https://comp21storage.z5.web.core.windows.net/comp23/filter/query.metadata.public.100K.spmat" "query.metadata.public.100K.spmat"
dl "https://comp21storage.z5.web.core.windows.net/comp23/filter/GT.public.ibin"               "GT.public.ibin"
cd ..

############################################################
# 2. SIFT (1M, 100M, 1B) -- BIGANN dataset
############################################################
mkdir -p bigann && cd bigann
# SIFT1B raw (uint8 base, 128 dim) -- 120 GB
dl "https://big-ann-benchmarks.com/data/bigann/bigann_base.bvecs.gz" "bigann_base.bvecs.gz"
dl "https://big-ann-benchmarks.com/data/bigann/bigann_query.bvecs"    "bigann_query.bvecs"
dl "https://big-ann-benchmarks.com/data/bigann/bigann_gnd.tar.gz"     "bigann_gnd.tar.gz"
cd ..

############################################################
# 3. GIST1M (image features, 960-dim)
############################################################
mkdir -p gist && cd gist
dl "ftp://ftp.irisa.fr/local/texmex/corpus/gist.tar.gz" "gist.tar.gz"
cd ..

############################################################
# 4. DEEP10M (deep image features, 96-dim) -- via Hugging Face mirror
############################################################
mkdir -p deep && cd deep
# Canonical DEEP1B project pages mirror the 10M slice via Yandex Disk;
# we use the deep-image-96-angular bench distribution.
dl "https://ann-benchmarks.com/deep-image-96-angular.hdf5" "deep_image_96_angular.hdf5"
cd ..

############################################################
# 5. LAION1M (1M CLIP-feature subset, 512-dim)
############################################################
mkdir -p laion1m && cd laion1m
# Canonical LAION distribution shards (parquet) need filtering; we ship
# preparation scripts in python/data_prep/laion_prepare.py.
echo "[laion1m] download LAION-400M parquet shards from"
echo "  https://huggingface.co/datasets/laion/laion400m"
echo "  then run: python python/data_prep/laion_prepare.py --shards N"
cd ..

cat <<EOM

============================================================
Dataset download summary
============================================================
Downloaded into: $DATA
Expected layout (after extraction):

  $DATA/yfcc100m/
      base.10M.u8bin                       (1.9 GB)
      query.public.100K.u8bin              (19 MB)
      base.metadata.10M.spmat              (~580 MB)
      query.metadata.public.100K.spmat
      GT.public.ibin
  $DATA/bigann/
      bigann_base.bvecs                    (120 GB -- decompress .bvecs.gz)
      bigann_query.bvecs
      bigann_gnd/                          (untarred ground truth)
  $DATA/gist/
      gist_base.fvecs etc                  (untarred)
  $DATA/deep/
      deep_image_96_angular.hdf5
  $DATA/laion1m/
      (populated by python/data_prep/laion_prepare.py)

Once the raw datasets are in place, the SIFT100M / SIFT1B variants are derived
by python/data_prep/prepare_sift100m_unified.py and
python/data_prep/sift100m_build_conjunction_data.py.

YFCC10M conjunction ground truth for slices [50K,60K), [60K,70K), ..., [90K,100K)
is generated by python/data_prep/gen_yfcc10m_gt_matched_v6.py.
EOM
